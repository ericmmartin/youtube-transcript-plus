import { listLanguages } from 'youtube-transcript-plus';

async function main() {
  try {
    const videoId = 'dQw4w9WgXcQ';
    const languages = await listLanguages(videoId);

    console.log('Available caption languages:');
    languages.forEach((lang) => {
      const autoTag = lang.isAutoGenerated ? ' (auto-generated)' : '';
      console.log(`  ${lang.languageCode} - ${lang.languageName}${autoTag}`);
    });
  } catch (error) {
    console.error('Error listing languages:', error.message);
  }
}

main();
